#!/usr/bin/env node
/**
 * Regenerate lib/ziwei/grid-brightness-tables.generated.ts from production scrape data.
 * Source: lib/ziwei/data/grid-brightness-scrape.json
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const scrape = JSON.parse(
  readFileSync(join(root, 'lib/ziwei/data/grid-brightness-scrape.json'), 'utf8'),
);

function toTsRow(values) {
  return values
    .map(v => (v == null ? 'undefined' : `'${v}'`))
    .join(', ');
}

function toTsTable(table) {
  const lines = Object.entries(table).map(
    ([star, row]) => `  '${star}': [${toTsRow(row)}],`,
  );
  return `{\n${lines.join('\n')}\n}`;
}

const out = `// AUTO-GENERATED by scripts/build-grid-brightness.mjs — do not edit manually
// ${scrape.source}
// Indexing: ${scrape.indexing}

export const GRID_MAJOR_BRIGHTNESS: Record<string, (string | undefined)[]> = ${toTsTable(scrape.major)};

export const GRID_AUX_BRIGHTNESS: Record<string, (string | undefined)[]> = ${toTsTable(scrape.aux)};
`;

const target = join(root, 'lib/ziwei/grid-brightness-tables.generated.ts');
writeFileSync(target, out);
console.log(`Wrote ${target}`);
